package com.originsys.realtygis.domain;

public class CountyLZCountAndAreaBean {
	private String code;
	private String name;
	private String count;
	private String area;
	public CountyLZCountAndAreaBean(){}
	public CountyLZCountAndAreaBean(String code, String name, String count,
			String area) {
		super();
		this.code = code;
		this.name = name;
		this.count = count;
		this.area = area;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	
	
	
}
