package com.originsys.datacenter.domain;
import java.io.Serializable;

public class YcDatacenterServiceParams implements Serializable{
	/**参数id*/
	private String param_id;
	/**服务id*/
	private String service_id;
	/**参数名称*/
	private String param_code;
	/**参数描述*/
	private String param_desc;
	/**参数类型* 入参0，返回参数1*/
	private String param_type;
	/**是否必须 0不是必须，1是必须的，入参的时候设置*/
	private String is_required;
	/**字段类型 varchar,int，date等*/
	private String field_type;
	//----------------------------------------------------------------
	/**设置参数id的get方法*/
	public String getParam_id() {
		return param_id;
	}
	/**设置参数id的set方法*/
	public void setParam_id(String param_id) {
		this.param_id = param_id;
	}
	/**设置服务id的get方法*/
	public String getService_id() {
		return service_id;
	}
	/**设置服务id的set方法*/
	public void setService_id(String service_id) {
		this.service_id = service_id;
	}
	/**设置参数名称的get方法*/
	public String getParam_code() {
		return param_code;
	}
	/**设置参数名称的set方法*/
	public void setParam_code(String param_code) {
		this.param_code = param_code;
	}
	/**设置参数描述的get方法*/
	public String getParam_desc() {
		return param_desc;
	}
	/**设置参数描述的set方法*/
	public void setParam_desc(String param_desc) {
		this.param_desc = param_desc;
	}
	/**设置参数类型的get方法*/
	public String getParam_type() {
		return param_type;
	}
	/**设置参数类型的set方法*/
	public void setParam_type(String param_type) {
		this.param_type = param_type;
	}
	/**设置是否必须的get方法*/
	public String getIs_required() {
		return is_required;
	}
	/**设置是否必须的set方法*/
	public void setIs_required(String is_required) {
		this.is_required = is_required;
	}
	/**设置字段类型的get方法*/
	public String getField_type() {
		return field_type;
	}
	/**设置字段类型的set方法*/
	public void setField_type(String field_type) {
		this.field_type = field_type;
	}
}