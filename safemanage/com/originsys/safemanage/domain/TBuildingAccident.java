package com.originsys.safemanage.domain;
import java.io.Serializable;
import java.util.Date;

/**事故记录对象*/
public class TBuildingAccident implements Serializable{
	/**事故记录编号*/
	private String accident_id;
	/**楼幢编号*/
	private String building_id;
	/**事故标题*/
	private String accident_name;
	/**事故发生时间*/
	private Date accident_date;
	/**事故描述*/
	private String accident_description;
	/**事故类型*/
	private String accident_type;
	/**事故上报人*/
	private String accident_reporter;
	/**备注*/
	private String accident_note;
	//----------------------------------------------------------------
	public String getAccident_id() {
		return accident_id;
	}
	public void setAccident_id(String accident_id) {
		this.accident_id = accident_id;
	}
	public String getBuilding_id() {
		return building_id;
	}
	public void setBuilding_id(String building_id) {
		this.building_id = building_id;
	}
	public String getAccident_name() {
		return accident_name;
	}
	public void setAccident_name(String accident_name) {
		this.accident_name = accident_name;
	}
	public Date getAccident_date() {
		return accident_date;
	}
	public void setAccident_date(Date accident_date) {
		this.accident_date = accident_date;
	}
	public String getAccident_description() {
		return accident_description;
	}
	public void setAccident_description(String accident_description) {
		this.accident_description = accident_description;
	}
	public String getAccident_type() {
		return accident_type;
	}
	public void setAccident_type(String accident_type) {
		this.accident_type = accident_type;
	}
	public String getAccident_reporter() {
		return accident_reporter;
	}
	public void setAccident_reporter(String accident_reporter) {
		this.accident_reporter = accident_reporter;
	}
	public String getAccident_note() {
		return accident_note;
	}
	public void setAccident_note(String accident_note) {
		this.accident_note = accident_note;
	}
	
}